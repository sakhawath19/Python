# substituting the argument 2 for x
print((lambda x: x)(2))

# lambda function with one argument
x = lambda a: a + 10
print(x(5))

# lambda function with two arguments
y = lambda a, b: a * b
print(y(2, 3))

# lambda function with three arguments
z = lambda a, b, c: a + b + c
print(z(2, 3, 4))






